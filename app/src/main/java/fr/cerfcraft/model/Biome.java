package fr.cerfcraft.model;

public class Biome {

    int id;
    String name;
    String image;
    String type;
    String rarity;
    float temperature;
    String description;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getImage() {
        return image;
    }

    public String getType() {
        return type;
    }

    public String getRarity() {
        return rarity;
    }

    public float getTemperature() {
        return temperature;
    }

    public String getDescription() {
        return description;
    }
}
